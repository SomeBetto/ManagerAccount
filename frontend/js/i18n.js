export function i18n(key, placeholders = {}) {
    let text = (translations[currentLang] && translations[currentLang][key]) || (translations['en'] && translations['en'][key]) || key;
    for (const [pk, pv] of Object.entries(placeholders)) {
        text = text.replace(`{${pk}}`, pv);
    }
    return text;
}

export function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('appLang', lang);
    applyTranslations();
}

export function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (key) {
            el.innerText = i18n(key);
        }
    });
}
